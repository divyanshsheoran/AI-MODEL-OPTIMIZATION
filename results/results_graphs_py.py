import matplotlib.pyplot as plt
import numpy as np

# Data
models = [
    "Teacher\n(Baseline)",
    "Dynamic\nQuantized",
    "Pruned",
    "Student\n(KD)"
]

accuracies = [58, 58, 15.7, 55.6]
sizes = [9.19, 9.16, 18.02, 4.04]

x = np.arange(len(models))
width = 0.6

#  Accuracy Graph
fig, ax = plt.subplots(figsize=(8,6))
bars = ax.bar(x, accuracies, width, color=['#4CAF50', '#2196F3', '#FFC107', '#9C27B0'])

# Add labels on bars
for bar in bars:
    height = bar.get_height()
    ax.annotate(f'{height:.1f}%',
                xy=(bar.get_x() + bar.get_width() / 2, height),
                xytext=(0,3),  # 3 points vertical offset
                textcoords="offset points",
                ha='center', va='bottom')

ax.set_ylabel('Accuracy (%)')
ax.set_title(' Model Accuracy Comparison')
ax.set_xticks(x)
ax.set_xticklabels(models)
ax.set_ylim(0, 100)

plt.tight_layout()
plt.savefig("accuracy_comparison.png", dpi=300)
plt.show()


#  Size Graph
fig, ax = plt.subplots(figsize=(8,6))
bars = ax.bar(x, sizes, width, color=['#4CAF50', '#2196F3', '#FFC107', '#9C27B0'])

for bar in bars:
    height = bar.get_height()
    ax.annotate(f'{height:.2f} MB',
                xy=(bar.get_x() + bar.get_width() / 2, height),
                xytext=(0,3),
                textcoords="offset points",
                ha='center', va='bottom')

ax.set_ylabel('Model Size (MB)')
ax.set_title(' Model Size Comparison')
ax.set_xticks(x)
ax.set_xticklabels(models)
ax.set_ylim(0, max(sizes) + 5)

plt.tight_layout()
plt.savefig("size_comparison.png", dpi=300)
plt.show()

